// https://leetcode.com/problems/number-of-islands/

const numIslands = (grid) => {
    let result = 0
    const ROW = grid.length
    const COL = grid[0].length
    const visited = {}
    const directions = [
        [-1, 0],
        [0, 1],
        [1, 0],
        [0, -1]
    ] // up right down left

    const islandMarker = (row, col) => {
        visited[`${row}-${col}`] = 1

        directions.forEach((direction) => {
            const [x, y] = direction
            const nextR = row + x
            const nextC = col + y

            if (
                nextR < ROW &&
                nextR >= 0 &&
                nextC < COL &&
                nextC >= 0 &&
                !visited[`${nextR}-${nextC}`] &&
                grid[nextR][nextC] === '1'
            ) {
                islandMarker(nextR, nextC)
            }
        })
    }

    for (let i = 0; i < ROW; i++) {
        for (let j = 0; j < COL; j++) {
            if (!visited[`${i}-${j}`] && grid[i][j] === '1') {
                result += 1
                islandMarker(i, j)
            }
        }
    }
    return result
}

